function searchSolutions() {
    const searchInput = document.getElementById("searchInput").value.toLowerCase();
    const solutionResults = document.getElementById("solutionResults");
    solutionResults.innerHTML = "";

    // Filter solutions to match partial text in the problem name
    const filteredSolutions = solutions.filter(solution => {
        const problemName = solution.problem.toLowerCase();
        return problemName.includes(searchInput); // Check if search term is part of the problem name
    });

    if (filteredSolutions.length === 0) {
        solutionResults.innerHTML = "<p>Kindly check your spelling or try rephrasing your search again.</p>";
    } else {
        // Group problems by unique solutions
        const groupedSolutions = {};

        filteredSolutions.forEach(solution => {
            const key = `${solution.solution}|${solution.sprayingintervals}|${solution.image}|${solution.availablepacksize}|${solution.pricerange}`;
            if (!groupedSolutions[key]) {
                groupedSolutions[key] = {
                    solution: solution.solution,
                    sprayingintervals: solution.sprayingintervals,
                    image: solution.image,
                    availablepacksize: solution.availablepacksize,
                    pricerange: solution.pricerange,
                    problems: []
                };
            }
            groupedSolutions[key].problems.push(solution);
        });

        // Display grouped solutions
        Object.values(groupedSolutions).forEach(group => {
            const groupDiv = document.createElement("div");
            groupDiv.classList.add("solution-group");

            groupDiv.innerHTML = `
                <h3>Problems:</h3>
                <ul>
                    ${group.problems.map(problem => `
                        <li>
                            <strong>${problem.problem}</strong><br>
                            ${problem.infestationimage
                                ? `<img src="${problem.infestationimage}" alt="${problem.problem} Infestation" style="max-width: 300px;">`
                                : `<p>No infestation image available.</p>`
                            }
                            <div style="background-color: #FCE2E1; padding: 5px; border-radius: 5px; margin-top: 10px;">
                                <p>${problem.description || "No additional details available."}</p>
                            </div>
                        </li>
                    `).join('')}
                </ul>
                <h3>Solution:</h3>
                <div style="background-color: #FDFBE5; padding: 5px; border-radius: 5px; margin-top: 10px;">
                    <p>${group.solution}</p>
                </div>
                <img src="${group.image}" alt="Solution Image" style="max-width: 300px;">
                <h3>Spraying Intervals:</h3>
                <div style="background-color: #FDFBE5; padding: 5px; border-radius: 5px; margin-top: 10px;">
                    <p>${group.sprayingintervals}</p>
                </div>
                <h3>Available Pack Size:</h3>
                <table>
                    <tr>
                        <th>Pack Size</th>
                        <th>Price Range (Ksh)</th>
                    </tr>
                    ${group.availablepacksize.split(',').map((size, index) => `
                        <tr>
                            <td>${size}</td>
                            <td>${group.pricerange.split(',')[index]}</td>
                        </tr>
                    `).join('')}
                </table>
            `;

            solutionResults.appendChild(groupDiv);
        });
    }
}





// Load TensorFlow.js model
async function loadModel() {
    try {
        const model = await tf.loadLayersModel('./model/model.json'); // Path from the model
        console.log("Model loaded successfully");
        return model;
    } catch (error) {
        console.error("Error loading the model:", error);
        alert("Failed to load the model. Please check if the model file exists.");
    }
}


// Preview the uploaded image
function previewImage(event) {
    const reader = new FileReader();
    reader.onload = function () {
        const imagePreview = document.getElementById('imagePreview');
        imagePreview.innerHTML = `<img src="${reader.result}" id="uploadedImage" alt="Uploaded Image" style="max-width: 300px;"/>`;
    };
    reader.readAsDataURL(event.target.files[0]);
}

// Upload and classify the image
async function uploadImage() {
    const loadingSpinner = document.getElementById('loadingSpinner');
    loadingSpinner.style.display = 'block'; // Show the loading spinner

    try {
        const fileInput = document.getElementById("fileInput");
        const file = fileInput.files[0];

        if (!file) {
            alert("Please select an image for diagnosis.");
            loadingSpinner.style.display = 'none';
            return;
        }

        const imageElement = document.getElementById('uploadedImage');
        const model = await loadModel();

        // Preprocess the image to the required size (224x224)
        const tensor = tf.browser.fromPixels(imageElement)
            .resizeNearestNeighbor([224, 224])
            .toFloat()
            .expandDims(0);

        // Perform classification
        const predictions = model.predict(tensor);
        const result = await predictions.array();

        const highestProbabilityIndex = result[0].indexOf(Math.max(...result[0]));

        // Display the result
        displayDiagnosis(highestProbabilityIndex);
    } finally {
        loadingSpinner.style.display = 'none'; // Hide the spinner after processing
    }
}


